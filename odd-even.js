// check even number

function isEven(number) {
    if (number % 2 == 0) {
        return true;
    }
    else {
        return false;
    }
}
var myNumber = 69;
var result = isEven(myNumber);
console.log(result);

var herNumber = 90;
var result = isEven(herNumber);
console.log(result);


// check odd number
function isOdd(number) {
    if (number % 2 == 1) {
        return true;
    }
    else {
        return false
    }
}
var myNumber = 50;
var result = isOdd(myNumber);
console.log(result);

var herNumber = 21;
var result = isOdd(herNumber);
console.log(result);